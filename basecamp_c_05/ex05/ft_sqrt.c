int		ft_sqrt(int nb)
{
	int i;

	i = 2;
	if (nb <= 0)
	{
		return (0);
	}
	if (nb == 1)
	{
		return (1);
	}
	if (nb <= 3)
	{
		return (0);
	}
	while (i * i < nb && i < 46341)
	{
		i++;
	}
	if (nb % i == 0)
	{
		return (i);
	}
	return (0);
}
